package model;

public class Nilai {
    private String kodejurusan, tahunpel, kodekelas, nis, idmapel, nilaisiswa;

    public String getKodejurusan() {
        return kodejurusan;
    }

    public void setKodejurusan(String kodejurusan) {
        this.kodejurusan = kodejurusan;
    }

    public String getTahunpel() {
        return tahunpel;
    }

    public void setTahunpel(String tahunpel) {
        this.tahunpel = tahunpel;
    }

    public String getKodekelas() {
        return kodekelas;
    }

    public void setKodekelas(String kodekelas) {
        this.kodekelas = kodekelas;
    }

    public String getNis() {
        return nis;
    }

    public void setNis(String nis) {
        this.nis = nis;
    }

    public String getIdmapel() {
        return idmapel;
    }

    public void setIdmapel(String idmapel) {
        this.idmapel = idmapel;
    }

    public String getNilaisiswa() {
        return nilaisiswa;
    }

    public void setNilaisiswa(String nilaisiswa) {
        this.nilaisiswa = nilaisiswa;
    }
}
